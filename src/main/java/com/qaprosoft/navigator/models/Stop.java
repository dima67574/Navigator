package com.qaprosoft.navigator.models;

public class Stop {
	private String number;
	private City city;
	public Stop() {}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public City getCity() {
		return city;
	}
	public void setCity(City city) {
		this.city = city;
	}
}
